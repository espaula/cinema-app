package com.br.cinema.model;

public class FilmeModel 
{
	private int idfilme;
	private String nome_filme;
	private String ano_producao;
	
	public int getIdfilme() {
		return idfilme;
	}
	public void setIdfilme(int idfilme) {
		this.idfilme = idfilme;
	}
	public String getNome_filme() {
		return nome_filme;
	}
	public void setNome_filme(String nome_filme) {
		this.nome_filme = nome_filme;
	}
	public String getAno_producao() {
		return ano_producao;
	}
	public void setAno_producao(String ano_producao) {
		this.ano_producao = ano_producao;
	}
	
}
